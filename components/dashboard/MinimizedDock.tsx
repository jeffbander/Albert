export default function MinimizedDock() {
  const { state, restorePanel, closePanel } = useDashboard();
  const minimizedPanels = state.panels.filter(p => p.isMinimized);

  if (minimizedPanels.length === 0) {
    return null;
  }

  return (
    <div className="fixed bottom-4 left-1/2 -translate-x-1/2 flex items-center gap-2 px-3 py-2 bg-gray-800/90 backdrop-blur border border-gray-700 rounded-full shadow-lg z-50">
      {minimizedPanels.map((panel) => (
        <div key={panel.id} className="relative group">
          <button
            onClick={() => restorePanel(panel.id)}
            className="flex items-center gap-2 px-3 py-1.5 bg-gray-700 hover:bg-gray-600 rounded-full transition"
            title={`Restore ${panel.title}`}
          >
            <span className="text-gray-300">{PANEL_ICONS[panel.type]}</span>
            <span className="text-sm text-gray-300 max-w-[100px] truncate">{panel.title}</span>
          </button>
          <button
            onClick={(e) => {
              e.stopPropagation();
              closePanel(panel.id);
            }}
            className="absolute -top-1 -right-1 w-4 h-4 bg-gray-600 hover:bg-red-500 rounded-full flex items-center justify-center opacity-0 group-hover:opacity-100 transition"
          >
            <svg className="w-2.5 h-2.5 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={3} d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        </div>
      ))}
    </div>
  );
}
